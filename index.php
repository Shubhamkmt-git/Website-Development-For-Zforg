    animate();
  });

  card.addEventListener("mouseleave", ()=>{
    hovering = false;
    cancelAnimationFrame(frame);

    img.style.transition = "transform 0.4s ease";
    img.style.transform = "translateY(0px)";
    position = 0;

    setTimeout(()=>{
