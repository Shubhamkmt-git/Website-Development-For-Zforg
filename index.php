const row1 = document.getElementById("row1Wrapper");
const row2 = document.getElementById("row2Wrapper");

let maxMove1 = 0;
let maxMove2 = 0;

function calculateWidths(){

  if(!row1 || !row2) return;

  const containerWidth1 = row1.parentElement.clientWidth;
  const containerWidth2 = row2.parentElement.clientWidth;

  maxMove1 = row1.scrollWidth - containerWidth1;
  maxMove2 = row2.scrollWidth - containerWidth2;
}

function animateRows(){

  if(!row1 || !row2) return;

  // Disable animation on mobile
  if(window.innerWidth < 768){
    row1.style.transform = "translateX(0px)";
    row2.style.transform = "translateX(0px)";
    return;
  }

  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
  if(maxScroll <= 0) return;

  const progress = window.scrollY / maxScroll;

  const offset1 = -180;
  const offset2 = -500;

  const move1 = -progress * maxMove1 - offset1;
  const move2 = maxMove2 * (progress - 1) + offset2;

  row1.style.transform = `translateX(${move1}px)`;
  row2.style.transform = `translateX(${move2}px)`;
}


// Throttled scroll using requestAnimationFrame
let ticking = false;

window.addEventListener("scroll", () => {

  if (!ticking) {
    requestAnimationFrame(() => {
      animateRows();
      ticking = false;
    });
    ticking = true;
  }

});

window.addEventListener("resize", () => {
  calculateWidths();
  animateRows();
});

window.addEventListener("load", () => {
  calculateWidths();
  animateRows();
